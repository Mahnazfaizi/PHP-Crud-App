if(isset($_SESSION["loggedin"]) && $_SESSION["loggedin"] === true){
    header("location: index.php");
    exit;
}elseif(isset($_COOKIE['rememberme'])){
      // Decrypt cookie variable value
  $userid = decryptCookie($_COOKIE['rememberme']);

